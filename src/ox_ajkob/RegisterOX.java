        public static boolean checkID = false;
    
    public void checkUsername(){
        MongoClientURI uri = new MongoClientURI("mongodb://hummel26:hummel26@ds141942.mlab.com:41942/ox");
        MongoClient client = new MongoClient(uri);
        MongoDatabase db = client.getDatabase(uri.getDatabase());
        
        MongoCollection<Document> reg = db.getCollection("Register");
        
        try{
            Document myDoc = reg.find(eq("username", jTextField_username.getText())).first();
            System.out.println(myDoc.toJson());
            checkID = true;
        }catch(Exception e){
            System.out.println("username ไม่ซ้ำ");
            checkID = false;
        }
    }
    
    public boolean checkRepassword(){
        if(jPasswordField_password.getText().equals(jPasswordField_repassword.getText())){
            System.out.println("รหัสผ่านตรงกัน");
            return true;
        }else
            return false;
    }
    
    public boolean checkEmpty(){
        if(jTextField_firstname.getText().equals("")||
                jTextField_username.getText().equals("")||
                jPasswordField_password.getText().equals("")||
                jPasswordField_repassword.getText().equals("")){
            return true;
        }
        return false;
    }
    
    public void reset(){
        jTextField_firstname.setText("");
        jTextField_username.setText("");
        jPasswordField_password.setText("");
        jPasswordField_repassword.setText("");
    }

     public void submitRegister(){
        MongoClientURI uri = new MongoClientURI("mongodb://hummel26:hummel26@ds141942.mlab.com:41942/ox");
        MongoClient client = new MongoClient(uri);
        MongoDatabase db = client.getDatabase(uri.getDatabase());
        
        MongoCollection<Document> rgt = db.getCollection("Register");
        
        List<Document> seedData = new ArrayList<Document>();
        
        seedData.add(new Document("username",jTextField_username.getText())
                .append("password", jPasswordField_password.getText())
                .append("firstname", jTextField_firstname.getText()));
                
        
        rgt.insertMany(seedData);
        JLabel label = new JLabel("สมัครสมาชิกสำเร็จ");
        label.setFont(new Font("TH Sarabun New", Font.BOLD, 18));
        JOptionPane.showMessageDialog(this, label);
    }
                   
